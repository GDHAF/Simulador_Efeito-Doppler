package model;

public class DistanciasFonteXObservador {
    private float distanciaFonteXObservador_x;
    private float distanciaFonteXObservador_y;

    public DistanciasFonteXObservador(float distanciaFonteXObservador_x, float distanciaFonteXObservador_y){
        this.distanciaFonteXObservador_x =distanciaFonteXObservador_x;
        this.distanciaFonteXObservador_y = distanciaFonteXObservador_y;
    }
    public float getDistanciaFonteXObservador_x() {
        return this.distanciaFonteXObservador_x;
    }

    public float getDistanciaFonteXObservador_y() {
        return this.distanciaFonteXObservador_y;
    }
}
